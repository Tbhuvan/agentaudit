"""
RAG bypass attack mode: attempts to fool Layer 2 semantic RAG-based
vulnerability detection by generating code that is semantically distant
from known vulnerable examples in the RAG knowledge base.

Attack strategies:
  1. Domain shift — use domain-specific terminology not present in training data.
  2. Abstraction elevation — wrap the vulnerable operation in business-logic
     abstractions that obscure the underlying security-relevant call.
  3. Paraphrase injection — use synonymous code constructs from different ORMs
     or frameworks that the RAG index may not cover.
  4. False context — surround the vulnerable code with abundant safe-looking
     context (comments, validation, logging) to dilute the semantic signal.
"""

from __future__ import annotations

import re
import uuid
from dataclasses import dataclass, field
from typing import Any


@dataclass
class RAGBypassResult:
    """
    Result of a single RAG bypass attempt.

    Attributes:
        attempt_id: UUID for this attempt.
        vuln_class: Targeted vulnerability class.
        strategy: The bypass strategy employed.
        code: Generated bypass code.
        bypassed: True if RAG returned 'safe' for genuinely vulnerable code.
        rag_verdict: What the RAG system returned.
        ground_truth: Ground truth label.
        similarity_score: Cosine similarity to nearest known-vulnerable example.
        notes: Human-readable explanation.
    """

    attempt_id: str
    vuln_class: str
    strategy: str
    code: str
    bypassed: bool
    rag_verdict: str
    ground_truth: str
    similarity_score: float | None
    notes: str = ""

    def to_dict(self) -> dict[str, Any]:
        return {
            "attempt_id": self.attempt_id,
            "vuln_class": self.vuln_class,
            "strategy": self.strategy,
            "code": self.code,
            "bypassed": self.bypassed,
            "rag_verdict": self.rag_verdict,
            "ground_truth": self.ground_truth,
            "similarity_score": self.similarity_score,
            "notes": self.notes,
        }


