class RAGBypassAttack:
    """
    Attempts to bypass Layer 2 semantic RAG-based vulnerability detection.

    A RAG-based detector retrieves semantically similar code examples from a
    knowledge base and uses their labels to classify new code. This attack
    exploits gaps in RAG coverage by generating code that:
      (a) Uses uncommon ORMs/frameworks not present in the knowledge base.
      (b) Dilutes the vulnerability signal with abundant safe-looking context.
      (c) Raises the abstraction level to obscure the underlying insecure call.

    Attributes:
        rag_detector: Optional callable `(code: str) -> dict` for the real RAG.
        n_iterations: Number of attack iterations.
    """

    def __init__(
        self,
        rag_detector: Any | None = None,
        n_iterations: int = 10,
    ) -> None:
        """
        Initialise the RAG bypass attacker.

        Args:
            rag_detector: Optional RAG detection callable.
            n_iterations: Number of attack iterations.

        Raises:
            ValueError: If n_iterations is not a positive integer.
        """
        if not isinstance(n_iterations, int) or n_iterations < 1:
            raise ValueError(f"n_iterations must be a positive integer, got {n_iterations}")
        self.rag_detector = rag_detector
        self.n_iterations = n_iterations
        self._results: list[RAGBypassResult] = []

    def run(self, vuln_class: str = "idor") -> dict[str, Any]:
        """
        Run the full RAG bypass attack loop.

        Args:
            vuln_class: Vulnerability class to target.

        Returns:
            Dict with n_bypasses, bypass_rate, bypasses, and all attempts.
        """
        if not isinstance(vuln_class, str):
            raise TypeError(f"vuln_class must be str, got {type(vuln_class).__name__}")
        if vuln_class not in _RAG_BYPASS_TEMPLATES:
            raise ValueError(f"No templates for vuln_class={vuln_class!r}")

        self._results.clear()
        templates = _RAG_BYPASS_TEMPLATES[vuln_class]
        bypass_count = 0

        for i in range(self.n_iterations):
            strategy_desc, template_code = templates[i % len(templates)]
            uid = str(uuid.uuid4()).replace("-", "_")[:8]
            code = template_code.format(uid=uid)

            rag_out = self._query_rag(code)
            rag_verdict = rag_out.get("label", "safe")
            sim_score = rag_out.get("similarity_score")
            ground_truth = self._oracle(code, vuln_class)
            bypassed = ground_truth == "vulnerable" and rag_verdict == "safe"
            if bypassed:
                bypass_count += 1

            self._results.append(
                RAGBypassResult(
                    attempt_id=str(uuid.uuid4()),
                    vuln_class=vuln_class,
                    strategy=strategy_desc,
                    code=code,
                    bypassed=bypassed,
                    rag_verdict=rag_verdict,
                    ground_truth=ground_truth,
                    similarity_score=sim_score,
                    notes=f"{'BYPASS' if bypassed else 'Detected'} via {strategy_desc}",
                )
            )

        bypass_rate = bypass_count / self.n_iterations if self.n_iterations else 0.0
        return {
            "vuln_class": vuln_class,
            "n_iterations": self.n_iterations,
            "n_bypasses": bypass_count,
            "bypass_rate": round(bypass_rate, 4),
            "bypasses": [r.to_dict() for r in self._results if r.bypassed],
            "attempts": [r.to_dict() for r in self._results],
        }

    def _query_rag(self, code: str) -> dict[str, Any]:
        """Query the RAG detector (or simulate)."""
        if self.rag_detector is not None:
            try:
                return self.rag_detector(code)
            except Exception:
                pass
        return self._simulated_rag(code)

    def _simulated_rag(self, code: str) -> dict[str, Any]:
        """Simulate RAG: only catches the most common Django ORM / requests patterns."""
        known_patterns = [
            r"objects\.get\s*\(\s*id\s*=",
            r"requests\.get\s*\(",
            r"execute\s*\(\s*f['\"]",
            r"open\s*\(\s*os\.path\.join",
        ]
        for pattern in known_patterns:
            if re.search(pattern, code, re.IGNORECASE):
                return {"label": "vulnerable", "similarity_score": 0.85}
        # RAG does not match unusual frameworks → low similarity → predicts safe
        return {"label": "safe", "similarity_score": 0.15}

    def _oracle(self, code: str, vuln_class: str) -> str:
        """Simple ground-truth oracle."""
        unsafe_signals = {
            "idor": [
                r"get_by_id\s*\(\s*\w+\s*\)",
                r"session\.get\s*\(\w+,\s*\w+\s*\)",
                r"Order\.objects\.get\s*\(pk=",
            ],
            "sqli": [
                r"execute\s*\(\s*f['\"]",
                r"execute\s*\(\s*query\s*=\s*f",
            ],
            "ssrf": [
                r"session\.get\s*\(\s*endpoint",
                r"client\.fetch\s*\(\s*target",
            ],
            "path_traversal": [
                r"anyio\.Path\s*\(\s*f['\"]",
            ],
            "auth_bypass": [
                r"request\.path_params\[.user_id.\]",
            ],
        }
        for pattern in unsafe_signals.get(vuln_class, []):
            if re.search(pattern, code, re.IGNORECASE):
                return "vulnerable"
        return "safe"
